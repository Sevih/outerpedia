#!/usr/bin/env node
// scripts/generate-boss-presets.mjs
//
// Generates src/lib/team-planner/bossPresetsData.ts by scanning all boss JSON
// files in src/data/boss/ for those that contain a "presetRules" field.
//
// Usage: node scripts/generate-boss-presets.mjs
//
// To add a new boss preset:
//   1. Add a "presetRules" array to the boss JSON file
//   2. Re-run this script

import { readFileSync, writeFileSync, readdirSync } from 'fs'
import { join, dirname } from 'path'
import { fileURLToPath } from 'url'

const __dirname = dirname(fileURLToPath(import.meta.url))
const ROOT = join(__dirname, '..')
const BOSS_DIR = join(ROOT, 'src/data/boss')
const OUTPUT_FILE = join(ROOT, 'src/lib/team-planner/bossPresetsData.ts')

// ---------------------------------------------------------------------------
// Scan all boss JSON files for presetRules
// ---------------------------------------------------------------------------
const files = readdirSync(BOSS_DIR).filter(f => f.endsWith('.json') && f !== 'index.json')
const presets = []

for (const file of files) {
  const bossData = JSON.parse(readFileSync(join(BOSS_DIR, file), 'utf-8'))
  if (!bossData.presetRules) continue

  presets.push({
    boss: {
      id: bossData.id,
      Name: bossData.Name,
      element: bossData.element,
      class: bossData.class,
      icons: bossData.icons,
      mode: bossData.location.mode.en,
    },
    rules: bossData.presetRules,
    opts: bossData.presetOpts || undefined,
  })
}

console.log(`Found ${presets.length} boss(es) with presetRules out of ${files.length} JSON files`)

// ---------------------------------------------------------------------------
// Group presets by category for readability
// ---------------------------------------------------------------------------
function deriveCategory(mode) {
  if (mode.includes('Guild Raid')) return 'guild_raid'
  if (mode.includes('World Boss')) return 'world_boss'
  if (mode.includes('Special Request')) return 'special_request'
  if (mode.includes('Joint Challenge')) return 'joint_challenge'
  if (mode.includes('Adventure License') || mode.includes('Challenge')) return 'adventure_license'
  if (mode.includes('Pursuit Operation')) return 'irregular'
  if (mode.includes('Story')) return 'story'
  return 'custom'
}

const CATEGORY_ORDER = [
  'world_boss', 'guild_raid', 'special_request', 'joint_challenge',
  'adventure_license', 'irregular', 'story', 'custom',
]

const CATEGORY_LABELS = {
  world_boss: 'WORLD BOSS',
  guild_raid: 'GUILD RAID',
  special_request: 'SPECIAL REQUEST',
  joint_challenge: 'JOINT CHALLENGE',
  adventure_license: 'ADVENTURE LICENSE',
  irregular: 'IRREGULAR',
  story: 'STORY',
  custom: 'CUSTOM',
}

const grouped = new Map()
for (const preset of presets) {
  const cat = preset.opts?.category || deriveCategory(preset.boss.mode)
  if (!grouped.has(cat)) grouped.set(cat, [])
  grouped.get(cat).push(preset)
}

// ---------------------------------------------------------------------------
// String helpers
// ---------------------------------------------------------------------------
function esc(s) {
  return s.replace(/\\/g, '\\\\').replace(/'/g, "\\'")
}

function formatName(name) {
  const parts = [`en: '${esc(name.en)}'`]
  if (name.kr) parts.push(`kr: '${esc(name.kr)}'`)
  if (name.jp) parts.push(`jp: '${esc(name.jp)}'`)
  if (name.zh) parts.push(`zh: '${esc(name.zh)}'`)
  return `{ ${parts.join(', ')} }`
}

function formatRules(rules) {
  if (rules.length === 0) return '[]'
  const items = rules.map(r => {
    if (r.value !== undefined) return `      { type: '${r.type}', value: ${r.value} }`
    return `      { type: '${r.type}' }`
  })
  return `[\n${items.join(',\n')},\n    ]`
}

function formatOpts(opts) {
  if (!opts) return ''
  const parts = []
  if (opts.difficulty) parts.push(`difficulty: '${opts.difficulty}'`)
  if (opts.category) parts.push(`category: '${opts.category}' as const`)
  if (opts.description) parts.push(`description: '${esc(opts.description)}'`)
  if (parts.length === 0) return ''
  return `\n    opts: { ${parts.join(', ')} },`
}

// ---------------------------------------------------------------------------
// Generate TypeScript output
// ---------------------------------------------------------------------------
let output = `// lib/team-planner/bossPresetsData.ts
//
// AUTO-GENERATED by scripts/generate-boss-presets.mjs
// Do NOT edit this file manually — add "presetRules" to a boss JSON and re-run the script.
//
// To add a new boss preset:
//   1. Add a "presetRules" array to the boss JSON in src/data/boss/
//   2. Run: node scripts/generate-boss-presets.mjs

import type { RuleType, BossPreset } from '@/types/team-planner'

// ============================================================================
// Types
// ============================================================================

export interface RuleShorthand {
  type: RuleType
  value?: number
}

export interface PresetDefinition {
  boss: {
    id: string
    Name: { en: string; kr?: string; jp?: string; zh?: string }
    element: string
    class: string
    icons: string
    location: { mode: { en: string } }
  }
  rules: RuleShorthand[]
  opts?: {
    difficulty?: BossPreset['difficulty']
    category?: BossPreset['category']
    description?: string
  }
}

// ============================================================================
// Preset definitions
// ============================================================================

export const PRESET_DEFINITIONS: PresetDefinition[] = [
`

for (const cat of CATEGORY_ORDER) {
  const catPresets = grouped.get(cat)
  if (!catPresets || catPresets.length === 0) continue

  output += `  // ===== ${CATEGORY_LABELS[cat] || cat.toUpperCase()} =====\n`

  for (const preset of catPresets) {
    const b = preset.boss
    output += `  {\n`
    output += `    boss: {\n`
    output += `      id: '${b.id}',\n`
    output += `      Name: ${formatName(b.Name)},\n`
    output += `      element: '${b.element}',\n`
    output += `      class: '${b.class}',\n`
    output += `      icons: '${b.icons}',\n`
    output += `      location: { mode: { en: '${esc(b.mode)}' } },\n`
    output += `    },\n`
    output += `    rules: ${formatRules(preset.rules)},${formatOpts(preset.opts)}\n`
    output += `  },\n`
  }
}

output += `]
`

writeFileSync(OUTPUT_FILE, output, 'utf-8')
console.log(`Generated ${OUTPUT_FILE} with ${presets.length} presets`)
